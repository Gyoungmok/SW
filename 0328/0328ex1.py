num1=10 #num1 변수에 정수 10을 저장
num2=5 #num2 변수에 정수 5를 저장

res1=num1*num2 #res1변수에 num1*num2 결과 저장
res2=num1/num2 #res2변수에 num1/num2 결과 저장
res3=num1//num2 #res3변수에 num1//num2 결과 저장
res4=num1%num2 #res4변수에 num1%num2 결과 저장

print("num1*num2=",res1)
print("num1/num2=",res2)
print("num1//num2=",res3)
